// Helper function to get all cities in a region
export function getCitiesInRegion(regionName) {
  const region = MA_GEOGRAPHIC_DATA[regionName];
  if (!region) return [];
  
  const cities = [];
  Object.values(region.counties).forEach(county => {
    cities.push(...county.cities);
  });
  return cities;
}

// Helper function to get all cities in a county
export function getCitiesInCounty(regionName, countyName) {
  const region = MA_GEOGRAPHIC_DATA[regionName];
  if (!region) return [];
  
  const county = region.counties[countyName];
  return county ? county.cities : [];
}

// Helper function to get all zip codes for a city
export function getZipCodesForCity(cityName) {
  for (const region of Object.values(MA_GEOGRAPHIC_DATA)) {
    for (const county of Object.values(region.counties)) {
      const city = county.cities.find(c => c.name === cityName);
      if (city) return city.zipCodes;
    }
  }
  return [];
}

// Helper function to get region names
export function getRegionNames() {
  return Object.keys(MA_GEOGRAPHIC_DATA);
}

// Helper function to get county names in a region
export function getCountyNamesInRegion(regionName) {
  const region = MA_GEOGRAPHIC_DATA[regionName];
  return region ? Object.keys(region.counties) : [];
}

// Helper function to flatten all cities for autocomplete/search
export function getAllCities() {
  const allCities = [];
  for (const region of Object.values(MA_GEOGRAPHIC_DATA)) {
    for (const county of Object.values(region.counties)) {
      allCities.push(...county.cities);
    }
  }
  return allCities;
}
